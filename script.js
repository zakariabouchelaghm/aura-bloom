// Language Management
let currentLang = localStorage.getItem('aurabloom-lang') || 'en';

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('aurabloom-lang', lang);

    // Update HTML attributes
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';

    // Update all translatable elements
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (content[lang][key]) {
            element.innerHTML = content[lang][key];
        }
    });

    // Update language switcher
    updateLanguageSwitcher(lang);

    // Add transition class for smooth content change
    document.body.style.opacity = '0';
    setTimeout(() => {
        document.body.style.transition = 'opacity 0.3s ease';
        document.body.style.opacity = '1';
    }, 50);
}

function updateLanguageSwitcher(lang) {
    const toggle = document.getElementById('langToggle');
    const options = toggle.querySelectorAll('.lang-option');

    options.forEach(option => {
        if (option.getAttribute('data-lang') === lang) {
            option.classList.add('active');
        } else {
            option.classList.remove('active');
        }
    });

    if (lang === 'ar') {
        toggle.classList.add('ar');
    } else {
        toggle.classList.remove('ar');
    }
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    setLanguage(currentLang);

    // Language switcher event
    const langToggle = document.getElementById('langToggle');
    langToggle.addEventListener('click', () => {
        const newLang = currentLang === 'en' ? 'ar' : 'en';
        setLanguage(newLang);
    });

    // Scroll animations
    const observerOptions = {
        threshold: 0.1,
        rootMargin: '0px 0px -100px 0px'
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
            }
        });
    }, observerOptions);

    // Add fade-in class to sections
    const sections = document.querySelectorAll('section:not(.hero)');
    sections.forEach(section => {
        section.classList.add('fade-in');
        observer.observe(section);
    });

    // Smooth scroll for internal links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const target = document.querySelector(this.getAttribute('href'));
            if (target) {
                target.scrollIntoView({
                    behavior: 'smooth',
                    block: 'start'
                });
            }
        });
    });

    // Add parallax effect to hero
    let ticking = false;
    window.addEventListener('scroll', () => {
        if (!ticking) {
            window.requestAnimationFrame(() => {
                const scrolled = window.pageYOffset;
                const hero = document.querySelector('.hero');
                if (hero && scrolled < window.innerHeight) {
                    hero.style.transform = `translateY(${scrolled * 0.5}px)`;
                    hero.style.opacity = 1 - (scrolled / window.innerHeight);
                }
                ticking = false;
            });
            ticking = true;
        }
    });

    // Slideshow functionality
    let slideIndex = 1;
    let slideTimer;

    function showSlides(n) {
        let slides = document.getElementsByClassName("slide");
        let dots = document.getElementsByClassName("dot");

        if (n > slides.length) { slideIndex = 1 }
        if (n < 1) { slideIndex = slides.length }

        for (let i = 0; i < slides.length; i++) {
            slides[i].style.display = "none";
        }

        for (let i = 0; i < dots.length; i++) {
            dots[i].className = dots[i].className.replace(" active", "");
        }

        if (slides[slideIndex - 1]) {
            slides[slideIndex - 1].style.display = "block";
        }
        if (dots[slideIndex - 1]) {
            dots[slideIndex - 1].className += " active";
        }
    }

    // Make functions global for onclick handlers
    window.changeSlide = function (n) {
        clearTimeout(slideTimer);
        showSlides(slideIndex += n);
        startAutoSlide();
    }

    window.currentSlide = function (n) {
        clearTimeout(slideTimer);
        showSlides(slideIndex = n);
        startAutoSlide();
    }

    function startAutoSlide() {
        slideTimer = setTimeout(() => {
            slideIndex++;
            showSlides(slideIndex);
            startAutoSlide();
        }, 5000); // Change slide every 5 seconds
    }

    // Initialize slideshow
    showSlides(slideIndex);
    startAutoSlide();
});

